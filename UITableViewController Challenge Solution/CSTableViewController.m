#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 3;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    NSInteger filasPorSeccion;
    if (section == 0) {
        filasPorSeccion = 2;
    } else if (section == 1) {
        filasPorSeccion = 1;
    } else {
        filasPorSeccion = 3;
    }
    return filasPorSeccion;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Celda" forIndexPath:indexPath];
    
    // Configure the cell...
    if (indexPath.section == 0) {
        cell.textLabel.text = @"Estoy en la secci칩n 0";
        cell.textLabel.textColor = [UIColor redColor];
    } else if (indexPath.section == 1) {
        cell.textLabel.text = @"Otra secci칩n";
        cell.backgroundColor = [UIColor blueColor];
    } else {
        cell.textLabel.text = [NSString stringWithFormat:@"Celda %i", indexPath.row
                               ];
        cell.backgroundColor = [UIColor yellowColor];
    }
    
    return cell;
}


